public class Person {
    private int tuoi;
    private  String ten;
    public Person(int tuoi,String ten){
        this.tuoi = tuoi;
        this.ten = ten;
    }

    public int getTuoi() {
        return tuoi;
    }

    public void setTuoi(int tuoi) {
        this.tuoi = tuoi;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

}
